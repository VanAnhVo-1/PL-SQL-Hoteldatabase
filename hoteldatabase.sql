create or replace FUNCTION FUNC_RANDOM_PHONG(MALOAIPHONG_IN IN INT,
                                            NGAYDEN_IN IN DATE,
                                            NGAYROI_IN IN DATE
                                            )
RETURN NUMBER
IS
  PHONG_TRONG INT:= 0;
BEGIN
  SELECT DISTINCT MAPHONG INTO PHONG_TRONG FROM
    (SELECT MALOAIPHONG, MAPHONG FROM PHONG WHERE TINHTRANG <> 'S?A CH?A' 
      MINUS
    (SELECT DISTINCT P.MALOAIPHONG, PDT.MAPHONG FROM PHIEUDATTRUOC PDT INNER JOIN PHONG P
      ON PDT.MAPHONG = P.MAPHONG WHERE (NGAYDEN_IN <= NGAYDEN AND NGAYDEN <= NGAYROI_IN)
                                    OR (NGAYDEN_IN <= NGAYDEN AND NGAYROI <= NGAYROI_IN)
                                    OR (NGAYDEN_IN <= NGAYROI AND NGAYROI <= NGAYROI_IN)
      UNION 
      SELECT DISTINCT P1.MALOAIPHONG, PNP.MAPHONG FROM PHIEUNHANPHONG PNP INNER JOIN PHONG P1
        ON P1.MAPHONG = PNP.MAPHONG WHERE (NGAYDEN_IN <= NGAYDEN AND NGAYDEN <= NGAYROI_IN)
                                    OR (NGAYDEN_IN <= NGAYDEN AND NGAYROI <= NGAYROI_IN)
                                    OR (NGAYDEN_IN <= NGAYROI AND NGAYROI <= NGAYROI_IN)
    ))A
    WHERE MALOAIPHONG = MALOAIPHONG_IN
    ORDER BY DBMS_RANDOM.VALUE
    FETCH FIRST 1 ROW ONLY;
  RETURN PHONG_TRONG;
END;

