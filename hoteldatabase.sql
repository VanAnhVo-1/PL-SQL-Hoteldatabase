CREATE OR REPLACE FUNCTION FUNC_HOADON_TIENPHONG (MA_HD IN HOADONTHANHTOAN.MAHD%TYPE)
RETURN HOADONTHANHTOAN.TIENPHONG%TYPE
AS
  NGAY        INT;
  GIA_PHONG   LOAIPHONG.GIAPHONG%TYPE;
BEGIN
  SELECT NGAYROI - NGAYDEN INTO NGAY
  FROM HOADONTHANHTOAN
  WHERE MAHD = MA_HD;
  
  SELECT GIAPHONG INTO GIA_PHONG
  FROM HOADONTHANHTOAN H 
       JOIN PHIEUNHANPHONG N ON H.MAPHIEUNHAN = N.MAPHIEUNHAN
       JOIN PHONG P ON N.MAPHONG = P.MAPHONG
       JOIN LOAIPHONG L ON P.MALOAIPHONG = L.MALOAIPHONG
  WHERE MAHD = MA_HD;
  
  RETURN (GIA_PHONG * NGAY);
  
  EXCEPTION 
    WHEN NO_DATA_FOUND THEN RETURN 0;
END;

