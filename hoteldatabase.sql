CREATE OR REPLACE FUNCTION FUNC_HOADON_TIENGIAM (MA_HD IN HOADONTHANHTOAN.MAHD%TYPE)
RETURN HOADONTHANHTOAN.TIENGIAM%TYPE
AS
  NGAY_DEN    HOADONTHANHTOAN.NGAYDEN%TYPE;
  KHUYEN_MAI  NGAYLE.GIAM%TYPE;
  TIEN_PHONG  HOADONTHANHTOAN.TIENPHONG%TYPE;
  TIEN_GIAM INT;
BEGIN
  SELECT TIENPHONG, NGAYDEN INTO TIEN_PHONG, NGAY_DEN
  FROM HOADONTHANHTOAN
  WHERE MAHD = MA_HD;
  
  SELECT GIAM INTO KHUYEN_MAI
  FROM NGAYLE
  WHERE NGAYLE = NGAY_DEN FOR UPDATE;
  
  TIEN_GIAM := (TIEN_PHONG*(KHUYEN_MAI/100));
  RETURN TIEN_GIAM;
  
  EXCEPTION
    WHEN NO_DATA_FOUND THEN RETURN 0;
END;

