create or replace FUNCTION FUNC_CHECK_PHONGTRONG(MAPHONG_IN  INT,
                                                NGAYDEN_IN  DATE,
                                                NGAYROI_IN  DATE,
                                                MAPHIEU_IN INT)
RETURN NUMBER
IS
  CHECK_PHONGTRONG NUMBER := 0;
  V_COUNT INT;
BEGIN
  SELECT COUNT(*) INTO V_COUNT FROM 
    (SELECT MAPHIEUNHAN FROM PHIEUNHANPHONG WHERE MAPHONG = MAPHONG_IN AND ((NGAYDEN <= NGAYDEN_IN AND NGAYDEN_IN <= NGAYROI)
                                    OR (NGAYDEN <= NGAYDEN_IN AND NGAYROI_IN <= NGAYROI)
                                    OR (NGAYDEN <= NGAYROI_IN AND NGAYROI_IN <= NGAYROI))
      UNION 
    SELECT MAPHIEU FROM PHIEUDATTRUOC WHERE MAPHONG = MAPHONG_IN AND ((NGAYDEN <= NGAYDEN_IN AND NGAYDEN_IN <= NGAYROI)
                                    OR (NGAYDEN <= NGAYDEN_IN AND NGAYROI_IN <= NGAYROI)
                                    OR (NGAYDEN <= NGAYROI_IN AND NGAYROI_IN <= NGAYROI)) AND MAPHIEU <> MAPHIEU_IN)
                                    A;
  IF V_COUNT > 0 THEN
    CHECK_PHONGTRONG := 0;
  ELSE
    CHECK_PHONGTRONG := 1;
  END IF;
  RETURN CHECK_PHONGTRONG;
END;

DECLARE 
CHECK_PHONGTRONG NUMBER;
BEGIN
  CHECK_PHONGTRONG := FUNC_CHECK_PHONGTRONG(35,'28-JUN-2020','30-JUN-2020',91);
  DBMS_OUTPUT.PUT_LINE('PHÒNG TR?NG: ' || CHECK_PHONGTRONG);
END;

CREATE OR REPLACE PROCEDURE SLEEP(IN_TIME NUMBER)
AS
V_NOW DATE;
BEGIN
  SELECT SYSDATE INTO V_NOW FROM DUAL;
  LOOP 
  EXIT WHEN V_NOW + (IN_TIME * (1/86400)) <= SYSDATE;
  END LOOP;
END;

